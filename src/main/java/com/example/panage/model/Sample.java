package com.example.panage.model;

/**
 * @author panage
 */
public class Sample {
    private String hoge;
    private Long fuga;
    private Flag flag;

    public Sample() {
    }

    public String getHoge() {
        return hoge;
    }

    public void setHoge(String hoge) {
        this.hoge = hoge;
    }

    public Long getFuga() {
        return fuga;
    }

    public void setFuga(Long fuga) {
        this.fuga = fuga;
    }

    public Flag getFlag() {
        return flag;
    }

    public void setFlag(Flag flag) {
        this.flag = flag;
    }
}
